    'lifetime' => 0,

    /*
    |--------------------------------------------------------------------------
    | Session 文件路径
    |--------------------------------------------------------------------------
    |
    | 只有driver为file时才需要设置
    |
    */
    'files' => storage_path('framework/sessions'),

    /*
    |--------------------------------------------------------------------------
    | Session 数据表
    |--------------------------------------------------------------------------
    |
    | 使用 "数据库" 保存Session数据时，指定存储的数据表
