<?php
return [
    /*
    |--------------------------------------------------------------------------
    | 默认的session驱动
    |--------------------------------------------------------------------------
    |
    | 支持: "file", "database", "redis"
    |
    */
    'driver' => $_ENV['session']['driver'],

    /*
    |--------------------------------------------------------------------------
